var ctx = document.getElementById("myPieChart");
var myPieChart = new Chart(ctx, {
    type: 'doughnut',
    data: {
        labels: ["Paket Member", "Paket Free"],
        datasets: [{
            data: [10, 20],
            backgroundColor: ['#f6c23e', '#36b9cc'],
            hoverBackgroundColor: ['#d0a535', '#2c9faf'],
            hoverBorderColor: "rgba(234, 236, 244, 1)",
        }],
    },
    options: {
        maintainAspectRatio: false,
        tooltips: {
            backgroundColor: "rgb(255,255,255)",
            bodyFontColor: "#858796",
            borderColor: '#dddfeb',
            borderWidth: 1,
            xPadding: 15,
            yPadding: 15,
            displayColors: false,
            caretPadding: 10,
             callbacks: {
                    label: function(tooltipItem, chart) {
                        var datasetLabel = chart.datasets[tooltipItem.datasetIndex].label || '';
                        var dataLabel = chart.labels[tooltipItem.index];
                        var value = chart.datasets[tooltipItem.datasetIndex].data[tooltipItem.index];
                        var total = chart.datasets[0].data.reduce((acc, val) => acc + val, 0);
                        var percentage = ((value / total) * 100).toFixed(2) + '%';
                        return dataLabel + ': '+ percentage;
                    }
                }
        },
        legend: {
            display: false
        },
        cutoutPercentage: 80,
    },
});
</script>
